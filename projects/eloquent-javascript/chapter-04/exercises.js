// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step = 1) {
  let array = [];
    if(start === end){
  return array;
  }
    if(step > 0){
      for(let i = start; i <= end; i += step){
        array.push(i)
    }
  }
    return array; 
}


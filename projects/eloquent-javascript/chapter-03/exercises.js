function isEven(positive) {

  // return positive % 2 === 0

if(positive % 2 === 0){
    return true;
  }else if(positive % 2 === 1){
    return false;
  }else {
    return false
  }

};


////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let count = 0;
  for(let i = 0; i < string.length; i++){
    if(string[i] === char){
      count++
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  let count = 0;
  for(let i = 0; i < string.length; i++){
    if(string[i] === 'B'){
      count++;

    }
  }
  return count;
}
